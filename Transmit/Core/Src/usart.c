/* 启动中断接收，使能 LPUART1 中断 */
void UART_ReceiveInit(void)
{
    HAL_NVIC_EnableIRQ(LPUART1_IRQn);
    HAL_UART_Receive_IT(&hlpuart1, &rx_byte, 1);
}

/* 接收完成回调：存入环形缓冲区，重新启动下一次接收 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == LPUART1)
    {
        uint16_t next = (rx_head + 1) % UART_RX_BUF_SIZE;
        if (next != rx_tail)    /* 缓冲区未满 */
        {
            rx_buf[rx_head] = rx_byte;
            rx_head = next;
        }
        HAL_UART_Receive_IT(&hlpuart1, &rx_byte, 1);
    }
}

/* 主循环调用：从缓冲区读取一个字节，返回 0xFF 表示无数据 */
uint8_t UART_GetRxByte(void)
{
    uint8_t data = 0xFF;
    if (rx_tail != rx_head)
    {
        data = rx_buf[rx_tail];
        rx_tail = (rx_tail + 1) % UART_RX_BUF_SIZE;
    }
    return data;
}

/* 主循环调用：返回缓冲区中可读取的字节数 */
uint16_t UART_RxAvailable(void)
{
    return (rx_head - rx_tail + UART_RX_BUF_SIZE) % UART_RX_BUF_SIZE;
}

